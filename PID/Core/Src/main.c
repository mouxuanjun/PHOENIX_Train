void USB_send_motor_status(MotorStatus *status)
{
  char buffer[64];
//  float target_speed = (dr16_data.channel_0 / 660.0f) * 0.5f;
  float target_speed = 25;
  int len = sprintf(buffer, "%d,%.3f\r\n", status->Speed, target_speed);
  while (CDC_Transmit_FS((uint8_t*)buffer, len) == USBD_BUSY) {
    osDelay(1); // 避免死循环阻塞
  }
}

void pid_task(void const * argument){
  Pid_Init(&pid_speed, 70 , 2, 62, 0.001, NORMAL_PID); // 初始化PID速度环
  Pid_Init(&pid_angle, 0.19 , 0.000, 4, 0.001, NORMAL_PID); //初始化PID角度环 
  Pid_Enable_ZeroCrossingProtection(&pid_angle , 1, 4096); 
  Pid_Set_Max_Output(&pid_speed , 24999);
  Pid_Set_Max_Output(&pid_angle ,300); 
  Pid_Set_Target(&pid_angle,1000);
  while(1){
    //更新角度环
    Pid_Update(&pid_angle, motor_status.MechanicalAngle, pid_angle.target); // 更新PID角度环
    
    float current_speed = motor_status.Speed;
    pid_speed.target = pid_angle.output; // 角度环输出为速度环目标速度
    Pid_Update(&pid_speed, current_speed, pid_speed.target); // 更新PID
    Motor_CTL_Message_Send(MSG_VOLTAGE_LOW, (int)pid_speed.output, 0, 0, 0);
	osDelay(1); // 1ms
	  
	  
//	  Pid_Update(&pid_speed, motor_status.Speed, pid_speed.target);
//	  Motor_CTL_Message_Send(MSG_VOLTAGE_LOW, (int)pid_speed.output, 0, 0, 0);
//      osDelay(1); // 1ms
	  
	}

}

